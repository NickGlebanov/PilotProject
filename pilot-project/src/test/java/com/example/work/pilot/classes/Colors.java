package com.example.work.pilot.classes;

public class Colors {

    String red;
    String blue;
    String gray;
    String white;
    String yellow;
    String pink;


    public String getRed() {
        return red;
    }

    public Colors withRed(String red) {
        this.red = red;
        return this;
    }

    public String getBlue() {
        return blue;
    }

    public Colors withBlue(String blue) {
        this.blue = blue;
        return this;
    }

    public String getGray() {
        return gray;
    }

    public Colors withGray(String gray) {
        this.gray = gray;
        return this;
    }

    public String getWhite() {
        return white;
    }

    public Colors withWhite(String white) {
        this.white = white;
        return this;
    }

    public String getYellow() {
        return yellow;
    }

    public Colors withYellow(String yellow) {
        this.yellow = yellow;
        return this;
    }

    public String getPink() {
        return pink;
    }

    public Colors withPink(String pink) {
        this.pink = pink;
        return this;
    }
}
